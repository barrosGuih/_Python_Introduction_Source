#Escreva um programa que receba 3 n ́umeros reais e retorne a m ́edia aritm ́etica deles.

firstNumber = float(input("Digite um numero real: "))
secondNumber = float(input("Digite um segundo numero real: "))
thirdNumber = float(input("Digite um terceiro numero real: "))

average = (firstNumber + secondNumber + thirdNumber) / 3
print("Media aritmetica e:", average)